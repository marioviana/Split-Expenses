        Date date = null;
        try {
            date = format.parse(d);
        } catch (ParseException ex) {
            Logger.getLogger(AdicionarDespesa.class.getName()).log(Level.SEVERE, null, ex);
        }
        List<String> listaMor = moradoresList.getSelectedValuesList();

        this.apart.registarPagamento(date,this.despesa);
        ConsultarDespesa cd = new ConsultarDespesa (this.app,this.apart, this.despesa, this.tipo);
        cd.setLocationRelativeTo(this);
        cd.setVisible(true);
        setVisible(false); //you can't see me!
        dispose(); 

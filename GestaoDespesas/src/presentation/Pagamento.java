    private void updateList() {
        DefaultListModel <String> dlm = new DefaultListModel<String>();
        for(Map.Entry<String, Morador> entry : apart.getMoradores().entrySet()) {
            String key = entry.getKey();
            if (!key.equals(this.apart.getMail()))
                dlm.addElement(key);
        }
        moradoresList.setModel(dlm);
    }


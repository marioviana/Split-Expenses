    private void updateList() {
        String mail = this.apart.getMail();
        DefaultListModel <String> dlm = new DefaultListModel<String>();
        if(ex.isEmpty()){
        for(Map.Entry<Integer,Despesa>  entry : apart.getDespesas().entrySet()) {
            
            if (entry.getValue().getEstado()==0) {
                dlm.addElement(entry.getValue().getId()+" - N - "+entry.getValue().getNome());
            }
            else {
                dlm.addElement(entry.getValue().getId()+" - P - "+entry.getValue().getNome());
            }
        }
        }
        else{
            for(Map.Entry<Integer,Despesa>  entry : ex.entrySet()) {
            
            if (entry.getValue().getEstado()==0) {
                dlm.addElement(entry.getValue().getId()+" - N - "+entry.getValue().getNome());
            }
            else {
                dlm.addElement(entry.getValue().getId()+" - P - "+entry.getValue().getNome());
            }
        }
            
        }
        despesasList.setModel(dlm);
    }
    

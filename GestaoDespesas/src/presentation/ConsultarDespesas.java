    private void consultExpenseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultExpenseButtonActionPerformed
        String str = despesasList.getSelectedValue();
        if (str!=null) {
        String[] parts = str.split(" ");
        Integer d = Integer.parseInt(parts[0]);
        Despesa des = this.apart.getDespesas().get(d);
        ConsultarDespesa cd;
        if (des.getClass().getName().equals("business.Despesa"))
            cd = new ConsultarDespesa (this.app,this.apart, d, 1);
        else 
            cd = new ConsultarDespesa (this.app,this.apart, d, 0);
        cd.setLocationRelativeTo(this);
        cd.setVisible(true);
        setVisible(false); //you can't see me!
        dispose(); //Destroy the JFrame object
        }
    }//GEN-LAST:event_consultExpenseButtonActionPerformed

